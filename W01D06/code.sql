-- INNER JOIN ON ID
SELECT DANCE.ID,DANCE.NAME,MUSIC.ID,MUSIC.NAME
FROM DANCE
JOIN
MUSIC
ON DANCE.ID = MUSIC.ID

-- INNER JOIN ON NAME
SELECT DANCE.ID,DANCE.NAME,MUSIC.ID,MUSIC.NAME
FROM DANCE
JOIN
MUSIC
ON DANCE.NAME = MUSIC.NAME;

-- INNER JOIN ON 2 COLUMNS
SELECT DANCE.ID,DANCE.NAME,MUSIC.ID,MUSIC.NAME
FROM DANCE
JOIN
MUSIC
ON DANCE.NAME = MUSIC.NAME AND DANCE.ID = MUSIC.ID;

