-- LEFT JOIN ON HOTNAME=NAME
-- DANCE IS APPEARING FIRST, SO DANCE IS THE LEFT TABLE
SELECT DANCE.ID,DANCE.HOTNAME,MUSIC.ID,MUSIC.NAME
FROM DANCE
LEFT JOIN
MUSIC
ON DANCE.HOTNAME = MUSIC.NAME;

-- RIGHT JOIN ON HOTNAME=NAME
-- MUSIC IS THE RIGHT TABLE BECAUSE IT IS APPEARING LATER
SELECT DANCE.ID,DANCE.HOTNAME,MUSIC.ID,MUSIC.NAME
FROM DANCE
RIGHT JOIN
MUSIC
ON DANCE.HOTNAME = MUSIC.NAME;

